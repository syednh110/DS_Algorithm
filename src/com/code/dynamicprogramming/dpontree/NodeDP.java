package com.code.dynamicprogramming.dpontree;

public class NodeDP {

    int first;
    int second;

    public NodeDP(int first, int second) {
        this.first = first;
        this.second = second;
    }

    public int getFirst() {
        return first;
    }

    public int getSecond() {
        return second;
    }
}
